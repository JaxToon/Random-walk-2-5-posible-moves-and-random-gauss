import random
x = 0
y = 0
epsilon = 1
x_positions = []
y_positions = []
langkah = 100

for i in range (langkah):
    ex = random.gauss(0, 1)
    ey = random.gauss(0, 1)
    
    x += epsilon * ex
    y += epsilon * ey
    x_positions.append(x)
    y_positions.append(y)
    
plt.plot(x_positions, y_positions)
