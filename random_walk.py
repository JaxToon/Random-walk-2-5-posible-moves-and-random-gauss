import matplotlib.pyplot as plt
import random
import numpy as np

#2 Kemungkinan arah pergerakan
pos = [0, 0]

positions = [pos.copy()]

for i in range(100):
    direction = random.choice(["E", "W"])

    if direction == "E":
        pos[0] += 1
    elif direction == "W":
        pos[0] -= 1

    positions.append(pos.copy())

positions = np.array(positions)

# Buat plot
plt.plot(positions[:, 0], positions[:, 1])
plt.show()

